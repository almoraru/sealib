#include "sealib.h"

void	*sea_memchr(void *s, int c, size_t n)
{
  unsigned char *src = s;
  unsigned char target = c;

  while (n-- > 0)
    {
      if (*src == target)
        return ((void *)src);
      src++;
    }
  return (NULL);
}
