#include "sealib.h"

int	sea_strcmp(const char *s1, const char *s2)
{
  size_t i = 0;

  while (s1[i] == s2[i] && s1[i])
    i++;
  return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
