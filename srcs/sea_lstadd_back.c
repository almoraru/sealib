#include "sealib.h"

void	sea_lstadd_back(t_list **lst, t_list *new)
{
  t_list *last;

  if (!lst || !new)
    return ;
  if (*lst)
    {
      *lst = new;
      return ;
    }
  last = sea_lstlast(*lst);
  last->next = new;
}
