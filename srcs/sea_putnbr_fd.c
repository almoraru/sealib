#include "sealib.h"

void	sea_putnbr_fd(int n, int fd)
{
  unsigned int nb;

  nb = n;
  if (n < 0)
    {
      nb = -n;
      sea_putchar_fd('-', fd);
    }
  if (nb > 9)
    {
      sea_putnbr_fd(nb / 10, fd);
      sea_putnbr_fd(nb % 10, fd);
    }
  if (n >= -9 && n <= 9)
    sea_putchar_fd(nb + '0', fd);
}
