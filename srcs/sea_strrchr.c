#include "sealib.h"

char	*sea_strrchr(const char *s, int c)
{
  char *last = NULL;

  while (1)
    {
      if (*s == c)
        last = (char *)s;
      if (*s == 0)
        break;
      s++;
    }
  return (last);
}
