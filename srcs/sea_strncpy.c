#include "sealib.h"

char	*sea_strncpy(char *dest, const char *src, size_t dsize)
{
  char *d = dest;
  const char *s = src;
  while (*s && (size_t)(s - src) < dsize)
    *d++ = *s++;
  while ((size_t)(d - dest) < dsize)
    {
      *d = 0;
      d++;
    }
  return (dest);
}
