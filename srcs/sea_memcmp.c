#include "sealib.h"

int	sea_memcmp(const char *s1, const char *s2, size_t n)
{
  const char *end = s1 + n;

  while (s1 < end)
    {
      if (*s1 != *s2)
        return (*(unsigned const char*)s1 - *(unsigned const char*)s2);
      s1++;
      s2++;
    }
  return (0);
}
