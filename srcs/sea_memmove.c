#include "sealib.h"

void	*sea_memmove(void *dest, const void *src, size_t n)
{
  unsigned char *d = dest;
  const unsigned char *s = src;

  if (d > s)
    {
      while (n > 0)
        {
          n--;
          d[n] = s[n];
        }
    }
  else {
    while (n > 0)
      {
        *d++ = *s++;
        n--;
      }
  }
  return (dest);
}
